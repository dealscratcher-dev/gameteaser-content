/**
 * Writes ads-config.js from environment variables (Netlify / Vercel / GitHub Actions).
 */
const fs = require("fs");
const path = require("path");

const publisherId = process.env.ADSENSE_PUBLISHER_ID || "ca-pub-4190145625443935";
const enabled = process.env.ADSENSE_ENABLED !== "false";

const config = {
  enabled,
  publisherId,
  slots: {
    leaderboard: process.env.ADSENSE_SLOT_LEADERBOARD || "0000000000",
    incontent: process.env.ADSENSE_SLOT_INCONTENT || "0000000001",
    rectangle: process.env.ADSENSE_SLOT_RECTANGLE || "0000000002",
    footer: process.env.ADSENSE_SLOT_FOOTER || "0000000003",
  },
  autoAds: process.env.ADSENSE_AUTO_ADS !== "false",
  consentRequired: process.env.ADSENSE_CONSENT !== "false",
};

const out = `/** Generated by npm run build — ${new Date().toISOString()} */\nwindow.SEASON_RUSH_ADS = ${JSON.stringify(config, null, 2)};\n`;

const target = path.join(__dirname, "..", "ads-config.js");
fs.writeFileSync(target, out, "utf8");

const pubNum = publisherId.replace("ca-pub-", "");
const adsTxt = `google.com, pub-${pubNum}, DIRECT, f08c47fec0942fa0\n`;
fs.writeFileSync(path.join(__dirname, "..", "ads.txt"), adsTxt, "utf8");

console.log("Wrote ads-config.js and ads.txt");
